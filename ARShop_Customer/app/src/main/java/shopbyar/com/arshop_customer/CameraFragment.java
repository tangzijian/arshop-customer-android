    /**
     * Saves a JPEG {@link Image} into the specified {@link File}.
     */
    public static class ImageSaver implements Runnable {

        /**
         * The JPEG image
         */
        private final Image mImage;
        /**
         * The folder, into which the image is saved
         */
        private String mFolder;
        /**
         * Image name
         */
        private String mName;
        private Activity mActivity;
        public ImageSaver(Image image, String folder, String name, Activity a) {
            mImage = image;
            mFolder = folder;
            mName = name;
            mActivity = a;
        }

        @Override
        public void run() {
            if (!existOrCreateFolder()) return;

            File imageFile = createImageFile();
            if (imageFile == null) return;

            File metaFile = createMetaFile();
            if (metaFile == null) return;

            saveImageToFile(imageFile);

            saveMetaDataToFile(metaFile);

        }

        private void saveMetaDataToFile(File metaFile) {
            String meta = ImageMeta.getJSON();
            try {
                FileOutputStream output = new FileOutputStream(metaFile);
                output.write(meta.getBytes());
            } catch (IOException e) {
                Log.i(TAG, "write meta data failed");
                e.printStackTrace();
            }
        }

        private void saveImageToFile(File imageFile) {
            ByteBuffer buffer = mImage.getPlanes()[0].getBuffer();
            byte[] bytes = new byte[buffer.remaining()];
            buffer.get(bytes);
            FileOutputStream output = null;
            try {
                output = new FileOutputStream(imageFile);
                output.write(bytes);
            } catch (IOException e) {
                e.printStackTrace();
            } finally {
                mImage.close();
                if (null != output) {
                    try {
                        output.close();
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                }
            }
        }

        private boolean existOrCreateFolder() {
            File imageFileFolder = new File(mActivity.getExternalFilesDir(null), mFolder);
            if (!imageFileFolder.exists()) {
                if (!imageFileFolder.mkdir()) {
                    Log.i(TAG, "create file directory failed");
                    return false;
                }
            }
            return true;
        }

        @Nullable
        private File createImageFile() {
            File dir = mActivity.getExternalFilesDir(null);
            File imageFile = new File(mActivity.getExternalFilesDir(null), mFolder+"/"+mName);
            if (!imageFile.exists()) {
                try {
                    Log.i(TAG, "created file:" + imageFile);
                    imageFile.createNewFile();
                } catch (IOException e) {
                    Log.i(TAG, "created file failed:" + imageFile);
                    e.printStackTrace();
                    return null;
                }
            }
            return imageFile;
        }

        @Nullable
        private File createMetaFile() {
            File metaFile = new File(mActivity.getExternalFilesDir(null), mFolder+"/"+mName+".txt");
            if (!metaFile.exists()) {
                try {
                    Log.i(TAG, "created file:" + metaFile);
                    metaFile.createNewFile();
                } catch (IOException e) {
                    Log.i(TAG, "created file failed:" + metaFile);
                    e.printStackTrace();
                    return null;
                }
            }
            return metaFile;
        }

    }

    /**
     * Compares two {@code Size}s based on their areas.
     */
    static class CompareSizesByArea implements Comparator<Size> {

        @Override
        public int compare(Size lhs, Size rhs) {
            // We cast here to ensure the multiplications won't overflow
            return Long.signum((long) lhs.getWidth() * lhs.getHeight() -
                    (long) rhs.getWidth() * rhs.getHeight());
        }

    }

